package com.myc;

public class Employee {

	private int eo;
	private String ename;
	private int sal;
	public int getEo() {
		return eo;
	}
	public void setEo(int eo) {
		this.eo = eo;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public int getSal() {
		return sal;
	}
	public void setSal(int sal) {
		this.sal = sal;
	}

	
}
